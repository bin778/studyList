package day07;

public class ForTest {
	public static void main(String[] args) {
		System.out.println("for문 위쪽");
		// int num;
		
		for (int i = 0; i < 10; i++) {
			System.out.println(i);
			System.out.println("for문 안쪽 문장1");
			System.out.println("for문 안쪽 문장2");
		}
		
		System.out.println("for문 아래쪽");
	}
}
